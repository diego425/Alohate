//funcion para esconder y mostrar el contenido de los pisos
//planta baja
function esconder_planta_b(){
    document.getElementById('ocultar_planta_b').style.display = 'none';
    document.getElementById('despliegue_planta_b').style.display = 'block';
    document.getElementById('detalle_planta_b').style.display = 'none';
}

function presentar_planta_b(){
    document.getElementById('ocultar_planta_b').style.display = 'block';
    document.getElementById('despliegue_planta_b').style.display = 'none';
    document.getElementById('detalle_planta_b').style.display = 'block';
}

